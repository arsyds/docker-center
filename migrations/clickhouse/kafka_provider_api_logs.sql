CREATE TABLE clickhouse_local.kafka_provider_api_logs (
    `transaction_reference` String,
    `endpoint_url` String,
    `signature` String,
    `request_payload` String,
    `response_payload` String,
    `req_header` String,
    `resp_header` String,
    `http_status_code` String,
    `request_date` String,
    `request_timestamp` String,
    `response_timestamp` String,
    `provider_name` String,
    `adapter_name` String,
    `provider_channel_id` String,
    `channel` String,
    `error_message` String
) ENGINE = Kafka SETTINGS kafka_broker_list = 'broker-1:9092,broker-2:9092,broker-3:9092',
kafka_topic_list = 'provider-api-log',
kafka_format = 'JSONEachRow',
kafka_group_name = 'provider-api-log-ch',
kafka_num_consumers = 1,
kafka_thread_per_consumer = 1,
kafka_client_id = 'clickhouse-eagle-consumer';